void *memcpy(void *dest, const void *src, size_t n)
{
    void *retval = dest;
    for (; n >= sizeof(long); n-= sizeof(long), dest += sizeof(long), src += sizeof(long))
        *(long *)dest = *(long *)src;
    for (; n; n-= sizeof(char), dest += sizeof(char), src += sizeof(char))
        *(char *)dest = *(char *)src;
    return retval;
}

void *memset(void *dest, int src, size_t n)
{
    void *retval = dest;
    for (; n; n--)
        *(char *)dest++ = src;
    return retval;
}

